// ======================= Creating Tables Using Whil Loop ======================= //

// ============= Table Of "2" : ============= \\
console.log(`\nThis is the Table of '2':`);
let a = 1;

while (a <= 10) {
    console.log(`2 x ${a} = ${a * 2}`);
    a++;
}

